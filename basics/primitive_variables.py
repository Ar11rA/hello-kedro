# variable
import math

# integer
num = 31
yet_another_num = 10

print(num, type(num))
print(yet_another_num)
print(num + yet_another_num)

# float
dist = math.sqrt(32)
print(dist, type(dist))

# string
message = "Hello World!"
print(message, type(message))

# boolean
is_true = True
print(is_true, type(is_true))
print(not is_true)

# None
nothing = None
print(nothing, type(nothing))
