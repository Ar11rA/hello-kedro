import math

num1 = 923
num2 = 5

# normal operations
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 ** num2)

# modulo
print(num1 % num2)
print(num1 % 2)
print(num1 % 10)

# math module
print(math.sqrt(num1))
print(math.pow(num1, num2))
print(math.floor(num1 / num2))
print(math.ceil(num1 / num2))